#include "core_types.h"
#include "logger.h"
#include "include/logger_interface.h"
#include "dm_helpers.h"

void dc_conn_log_hex_linux(const uint8_t *hex_data, int hex_data_count)
{
	int i;

	if (hex_data)
		for (i = 0; i < hex_data_count; i++)
			DC_LOG_DEBUG("%2.2X ", hex_data[i]);
}

