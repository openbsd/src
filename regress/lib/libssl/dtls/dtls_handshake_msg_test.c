static int
dtls_handshake_msg_test(void)
{
	struct dtls12_handshake_msg *hs_msg = NULL;
	uint8_t *p;
	int i;
	CBB cbb;
	CBS cbs;
	int failed = 1;

	memset(&cbb, 0, sizeof(cbb));

	if ((hs_msg = dtls12_handshake_msg_new()) == NULL) {
		fprintf(stderr, "FAIL: dtls12_handshake_msg_new\n");
		goto failure;
	}
	if (!dtls12_handshake_msg_start(hs_msg, &cbb, SSL3_MT_CLIENT_HELLO,
	    0xcafe)) {
		fprintf(stderr, "FAIL: dtls12_handshake_msg_start\n");
		goto failure;
	}
	if (!CBB_add_space(&cbb, &p, 256)) {
		fprintf(stderr, "FAIL: CBB_add_space\n");
		goto failure;
	}
	for (i = 0; i < 256; i++)
		p[i] = i;
	if (!dtls12_handshake_msg_finish(hs_msg)) {
		fprintf(stderr, "FAIL: dtls12_handshake_msg_finish\n");
		goto failure;
	}

	dtls12_handshake_msg_data(hs_msg, &cbs);
	if (CBS_len(&cbs) != sizeof(dtls12_hs_msg_data)) {
		fprintf(stderr, "FAIL: handshake message data length = %zu, want %zu\n",
		    CBS_len(&cbs), sizeof(dtls12_hs_msg_data));
		goto failure;
	}
	if (memcmp(CBS_data(&cbs), dtls12_hs_msg_data, CBS_len(&cbs)) != 0) {
		fprintf(stderr, "FAIL: got handshake message data:\n");
		hexdump(CBS_data(&cbs), CBS_len(&cbs));
		fprintf(stderr, "want handshake message data:\n");
		hexdump(dtls12_hs_msg_data, CBS_len(&cbs));
	}

	dtls12_handshake_msg_content(hs_msg, &cbs);
	if (CBS_len(&cbs) != sizeof(dtls12_hm_content)) {
		fprintf(stderr, "FAIL: handshake message content length = %zu, want %zu\n",
		    CBS_len(&cbs), sizeof(dtls12_hm_content));
		goto failure;
	}
	if (memcmp(CBS_data(&cbs), dtls12_hm_content, CBS_len(&cbs)) != 0) {
		fprintf(stderr, "FAIL: got handshake message content:\n");
		hexdump(CBS_data(&cbs), CBS_len(&cbs));
		fprintf(stderr, "want handshake message content:\n");
		hexdump(dtls12_hm_content, CBS_len(&cbs));
	}

	if (!dtls12_handshake_msg_fragment_pending(hs_msg)) {
		fprintf(stderr, "FAIL: handshake message should have pending fragments\n");
		goto failure;
	}
	if (!dtls12_handshake_msg_fragment_build(hs_msg, 64, &cbs)) {
		fprintf(stderr, "FAIL: failed to build handshake message fragment 0\n");
		goto failure;
	}
	if (CBS_len(&cbs) != sizeof(dtls12_hs_msg_frag_0)) {
		fprintf(stderr, "FAIL: handshake message fragment 0 length = %zu, want %zu\n",
		    CBS_len(&cbs), sizeof(dtls12_hs_msg_frag_0));
		goto failure;
	}
	if (memcmp(CBS_data(&cbs), dtls12_hs_msg_frag_0, CBS_len(&cbs)) != 0) {
		fprintf(stderr, "FAIL: got handshake message fragment:\n");
		hexdump(CBS_data(&cbs), CBS_len(&cbs));
		fprintf(stderr, "want handshake message fragment:\n");
		hexdump(dtls12_hs_msg_frag_0, CBS_len(&cbs));
	}

	if (!dtls12_handshake_msg_fragment_build(hs_msg, 32, &cbs)) {
		fprintf(stderr, "FAIL: failed to build handshake message fragment 1\n");
		goto failure;
	}
	if (CBS_len(&cbs) != sizeof(dtls12_hs_msg_frag_1)) {
		fprintf(stderr, "FAIL: handshake message fragment 1 length = %zu, want %zu\n",
		    CBS_len(&cbs), sizeof(dtls12_hs_msg_frag_1));
		goto failure;
	}
	if (memcmp(CBS_data(&cbs), dtls12_hs_msg_frag_1, CBS_len(&cbs)) != 0) {
		fprintf(stderr, "FAIL: got handshake message 1 fragment:\n");
		hexdump(CBS_data(&cbs), CBS_len(&cbs));
		fprintf(stderr, "want handshake message 1 fragment:\n");
		hexdump(dtls12_hs_msg_frag_1, CBS_len(&cbs));
	}

	if (!dtls12_handshake_msg_fragment_build(hs_msg, 32, &cbs)) {
		fprintf(stderr, "FAIL: failed to rebuild handshake message fragment 1\n");
		goto failure;
	}
	if (CBS_len(&cbs) != sizeof(dtls12_hs_msg_frag_1)) {
		fprintf(stderr, "FAIL: handshake message fragment 1 length = %zu, want %zu\n",
		    CBS_len(&cbs), sizeof(dtls12_hs_msg_frag_1));
		goto failure;
	}
	if (memcmp(CBS_data(&cbs), dtls12_hs_msg_frag_1, CBS_len(&cbs)) != 0) {
		fprintf(stderr, "FAIL: got handshake message 1 fragment:\n");
		hexdump(CBS_data(&cbs), CBS_len(&cbs));
		fprintf(stderr, "want handshake message 1 fragment:\n");
		hexdump(dtls12_hs_msg_frag_1, CBS_len(&cbs));
	}

	if (!dtls12_handshake_msg_fragment_next(hs_msg)) {
		fprintf(stderr, "FAIL: failed to progress to next fragment\n");
		goto failure;
	}
	if (!dtls12_handshake_msg_fragment_pending(hs_msg)) {
		fprintf(stderr, "FAIL: handshake message should have pending fragments\n");
		goto failure;
	}

	if (!dtls12_handshake_msg_fragment_build(hs_msg, 128, &cbs)) {
		fprintf(stderr, "FAIL: failed to build handshake message fragment 2\n");
		goto failure;
	}
	if (CBS_len(&cbs) != sizeof(dtls12_hs_msg_frag_2)) {
		fprintf(stderr, "FAIL: handshake message fragment 2 length = %zu, want %zu\n",
		    CBS_len(&cbs), sizeof(dtls12_hs_msg_frag_2));
		goto failure;
	}
	if (memcmp(CBS_data(&cbs), dtls12_hs_msg_frag_2, CBS_len(&cbs)) != 0) {
		fprintf(stderr, "FAIL: got handshake message 2 fragment:\n");
		hexdump(CBS_data(&cbs), CBS_len(&cbs));
		fprintf(stderr, "want handshake message 2 fragment:\n");
		hexdump(dtls12_hs_msg_frag_2, CBS_len(&cbs));
	}

	if (!dtls12_handshake_msg_fragment_next(hs_msg)) {
		fprintf(stderr, "FAIL: failed to progress to next fragment\n");
		goto failure;
	}
	if (!dtls12_handshake_msg_fragment_pending(hs_msg)) {
		fprintf(stderr, "FAIL: handshake message should have pending fragments\n");
		goto failure;
	}

	if (!dtls12_handshake_msg_fragment_build(hs_msg, 128, &cbs)) {
		fprintf(stderr, "FAIL: failed to build handshake message fragment 3\n");
		goto failure;
	}
	if (CBS_len(&cbs) != sizeof(dtls12_hs_msg_frag_3)) {
		fprintf(stderr, "FAIL: handshake message fragment 3 length = %zu, want %zu\n",
		    CBS_len(&cbs), sizeof(dtls12_hs_msg_frag_3));
		goto failure;
	}
	if (memcmp(CBS_data(&cbs), dtls12_hs_msg_frag_3, CBS_len(&cbs)) != 0) {
		fprintf(stderr, "FAIL: got handshake message 3 fragment:\n");
		hexdump(CBS_data(&cbs), CBS_len(&cbs));
		fprintf(stderr, "want handshake message 3 fragment:\n");
		hexdump(dtls12_hs_msg_frag_2, CBS_len(&cbs));
	}

	if (!dtls12_handshake_msg_fragment_next(hs_msg)) {
		fprintf(stderr, "FAIL: failed to progress to next fragment\n");
		goto failure;
	}
	if (dtls12_handshake_msg_fragment_pending(hs_msg)) {
		fprintf(stderr, "FAIL: handshake message should not have pending fragments\n");
		goto failure;
	}
	if (dtls12_handshake_msg_fragment_next(hs_msg)) {
		fprintf(stderr, "FAIL: should have failed to progress to next fragment\n");
		goto failure;
	}

	if (!dtls12_handshake_msg_fragment_reset(hs_msg)) {
		fprintf(stderr, "FAIL: failed to reset handshake message fragments\n");
		goto failure;
	}
	if (!dtls12_handshake_msg_fragment_pending(hs_msg)) {
		fprintf(stderr, "FAIL: handshake message should have pending fragments after reset\n");
		goto failure;
	}

	failed = 0;

 failure:
	dtls12_handshake_msg_free(hs_msg);

	CBB_cleanup(&cbb);

	return failed;
}

int
main(int argc, char **argv)
{
	int failed = 0;

	failed |= dtls_handshake_msg_test();

	return failed;
}
